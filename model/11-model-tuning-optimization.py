# Model Tuning (Hyperparameter Tuning)
# Hyperparameters are the parameters that are not learned by the model. 
# They are set by the data scientist before training the model. 
# For example, the depth of a decision tree, the number of clusters in K-Means, the number of layers in a neural network, etc. 
# Hyperparameter tuning is the process of choosing the right values for these hyperparameters. 
# This can be done manually or automatically using techniques like Grid Search, Random Search, Bayesian Optimization, etc.
# Optimize model performance using techniques like Grid Search, Random Search, Bayesian Optimization, etc.
# For example, in a Random Forest model, you can tune hyperparameters like the number of trees, 
# the maximum depth of the trees, the minimum number of samples required to split a node, etc.
